package com.wakeup.wakeup.ObjectClass;

public class LeaderboardModel {
    private String name;
    private int score;

    public LeaderboardModel(String name, int score) {
        this.name = name;
        this.score = score;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getScore() {
        return score;
    }

    public void setScore(int score) {
        this.score = score;
    }
}
