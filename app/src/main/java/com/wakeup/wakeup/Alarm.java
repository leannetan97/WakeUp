package com.wakeup.wakeup;

public class Alarm {

    public String time;
    public String alarmName;
    public int game;


    public Alarm() {
    }

    public Alarm(String time, String alarmName, int game) {
        this.time = time;
        this.alarmName = alarmName;
        this.game = game;
    }
}
